package com.laioffer.travelplanner.planModel;

public class Origin {
    private double lat;
    private double lon;

    public Origin() {
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}